namespace LeaRun.WebApp.Areas.ProjectManageModule.Controllers
{
    public class PublicFileController : Controller
    {
        //
        // GET: /ProjectManageModule/PublicFile/
        RepositoryFactory<PM_PublicFile> repositoryfactory = new RepositoryFactory<PM_PublicFile>();
        PM_PublicFileBll FileBll = new PM_PublicFileBll();

        public ActionResult Index()
        {
            return View();
        }

        public ActionResult GridPageListJson(string keywords, string CompanyId, string DepartmentId, JqGridParam jqgridparam, string ParameterJson)
        {
            try
            {
                Stopwatch watch = CommonHelper.TimerStart();
                DataTable ListData = FileBll.GetPageList(keywords, ref jqgridparam, ParameterJson);
                var JsonData = new
                {
                    total = jqgridparam.total,
                    page = jqgridparam.page,
                    records = jqgridparam.records,
                    costtime = CommonHelper.TimerEnd(watch),
                    rows = ListData,
                };
                return Content(JsonData.ToJson());
            }
            catch (Exception ex)
            {
                Base_SysLogBll.Instance.WriteLog("", OperationType.Query, "-1", "异常错误：" + ex.Message);
                return null;
            }
        }

    }
